"""Here’s a custom transformer that acts much like the StandardScaler:"""

from sklearn.base import BaseEstimator, TransformerMixin
from sklearn.utils.validation import check_array, check_is_fitted

class StandardScalerClone(BaseEstimator, TransformerMixin):
  def __init__(self, with_mean=True): # no *args or **kwargs!
      self.with_mean = with_mean

  def fit(self, X, y=None):  # y is required even though we don't use it
      X = check_array(X) # checks that X is an array with finite float values
      self.mean_ = X.mean(axis=0)
      self.scale_ = X.std(axis=0)
      self.n_features_in_ = X.shape[1] # every estimator stores this in fit()
      return self # always return self!

  def transform(self, X):
      check_is_fitted(self) # looks for learned attributes (with trailing _)
      X = check_array(X)
      assert self.n_features_in_ == X.shape[1]
      if self.with_mean:
          X = X - self.mean_
      return X / self.scale_

"""The following code demonstrates custom transformer that uses a KMeans clusterer in the fit() method to identify the main clusters in the training data, and then uses rbf_kernel() in the transform() method to measure how similar each sample is to each cluster center:"""

from sklearn.cluster import KMeans

class ClusterSimilarity(BaseEstimator, TransformerMixin):
    def __init__(self, n_clusters=10, gamma=0.1, random_state=None):
        self.n_clusters = n_clusters
        self.gamma = gamma
        self.random_state = random_state

    def fit(self, X, y=None, sample_weight=None):
        self.kmeans_ = KMeans(self.n_clusters, n_init=10,
                              random_state=self.random_state)
        self.kmeans_.fit(X, sample_weight=sample_weight)
        return self # always return self!

    def transform(self, X):
        return rbf_kernel(X, self.kmeans_.cluster_centers_, gamma=self.gamma)

    def get_feature_names_out(self, names=None):
        return [f"Cluster {i} similarity" for i in range(self.n_clusters)]

cluster_simil = ClusterSimilarity(n_clusters=10, gamma=1., random_state=42) #similarity drops off very quickly as you move away from a center. (Higher gamma ⇒ narrower peaks; lower gamma ⇒ broader influence.)
